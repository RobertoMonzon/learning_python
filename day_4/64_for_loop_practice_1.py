# Using For loops, greet all members of a class by printing "Hello" + their name.
# For example: "Hello Maria"
# students_class = ["María", "José", "Carlos", "Martina", "Isabel", "Tomás", "Daniela"]

students_class = ["María", "José", "Carlos", "Martina", "Isabel", "Tomás", "Daniela"]

for name in students_class:
    print("Hello " + name)